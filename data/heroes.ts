// 获取英雄头像URL的函数
export const getHeroAvatarUrl = (hero: Hero, ossBaseUrl: string): string => {
  if (hero.avatar) {
    return hero.avatar;
  }
  return `${ossBaseUrl}/heroes/${hero.id}.jpg`;
};

// 搜索英雄的函数
export const searchHeroes = (heroes: Hero[], searchTerm: string): Hero[] => {
  const searchLower = searchTerm.toLowerCase();
  return heroes.filter((hero) => {
    return (
      hero.name.toLowerCase().includes(searchLower) ||
      hero.cnName.includes(searchTerm) ||
      hero.nickname?.toLowerCase().includes(searchLower)
    );
  });
};